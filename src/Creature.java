
public class Creature {

	private String creatureName;
	private int creatureId;

	public Creature() {
	}
	
	public Creature(String name, int id) {
		this.creatureName = name;
		this.creatureId = id;
	}
	
	public String getCreatureName() {
		return creatureName;
	}
	
	public int getCreatureId() {
		return creatureId;
	}
	
	

	
	
}
